import UIKit

/// Shared layout constants for card panel controllers.
public enum LMKCardPanelLayout {
    /// Maximum card width (420pt).
    public static let cardMaxWidth: CGFloat = 420
    /// Horizontal inset from screen edges (24pt).
    public static let cardHorizontalInset: CGFloat = 24
    /// Card height as a ratio of the container height (0.6).
    public static let cardMaxHeightRatio: CGFloat = 0.6
    /// Vertical slide offset for animate-in/out (20pt).
    public static let slideOffset: CGFloat = 20
}
