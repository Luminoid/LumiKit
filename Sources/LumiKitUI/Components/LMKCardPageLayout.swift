import UIKit

/// Shared layout constants for card page controllers.
public enum LMKCardPageLayout {
    /// Default header height (52pt).
    public static let headerHeight: CGFloat = 52
    /// SF Symbol point size for header buttons (16pt, sized for 32pt buttons).
    public static let symbolPointSize: CGFloat = 16
    /// SF Symbol weight for header buttons.
    public static let symbolWeight: UIImage.SymbolWeight = .medium
    /// Separator height (0.5pt hairline).
    public static let separatorHeight: CGFloat = 0.5
}
